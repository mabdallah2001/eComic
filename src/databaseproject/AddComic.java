    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:

        try {
            // make the result set scrolable forward/backward updatable
            if (isValidData()) {
                //if user input is valid, create SQL command to insert tuple
                String sqlCMD = "INSERT INTO COMIC VALUES (" + ISBNText.getText().trim()
                        + ",'" + priceText.getText() + "','" + nameText.getText() + "'," + volumeText.getText() + "," + issueText.getText() + "," + quantityText.getText() + ",'" + cmbPublisher.getSelectedItem() + "')";
                
                System.out.println(sqlCMD);
                
                int result = con.executePrepared(sqlCMD);
                
                if (result > 0) {
                    javax.swing.JLabel label = new javax.swing.JLabel("New comic added successfully.");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    
                    clearInputBoxes();
                } else {
                    // check validation errors
                }
                
                
            } else {

                javax.swing.JLabel label = new javax.swing.JLabel("Please fix validation errors...");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error adding new comic\n" + e.getMessage());
        }
    }//GEN-LAST:event_submitButtonActionPerformed

